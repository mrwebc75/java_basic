package variable_10;

public class Ex30_Value {

  public static void main(String[] args) {

    // 변수 num1에 값 정수 100을 저장하되 선언과 할당을 분리하시오.
    int num1;
    num1 = 100;

    // Long타입 정수
    long num2 = 123456789000000L;

    // 문자 하나를 저장
    char c = '가';

    // 진리값 - true 아니면 false
    boolean t = true;
    boolean f = false;

    System.out.println("1. num1 = " + "100");
    System.out.println("2. num2 = " + "123456789000000");
    System.out.println("3. c = " + "가");
    System.out.println("4. t = " + "true");
    System.out.println("5. f = " + "false");

  }// end of main()
}
